		<h1>Help</h1>
		<strong>About Lunchsparks.me</strong>
		<p>
			<strong>Q: What problem are you solving for me?</strong>
			<br />
			A: You know that increasing your business network can mean the difference between success and obscurity, whether it's to find a better job, raise money for your startup or sign a critical partnership. The problem so far was: how do you actually build your own business network? LetsLunch.com uses the time you have at lunch to have you meet other professionals relevant to you.
		</p>
		<p>
			<strong>Q: How does it work?</strong>
			<br />
			A: You tell us which days you are available for lunch. The day before, we will match you with someone for lunch, in your neighborhood and e-mail you. Then you just confirm and find out who you are meeting with and where.
		</p>
		<p>
			<strong>About the Lunches</strong>
		</p>
		<p>
			<strong>Q: Where do the lunches take place?</strong>
			<br />
			A: Lunchsparks.me will choose a restaurant location for you that is within acceptable distance for both you and the person we'll have you meet. You can setup your "lunch radius" on a map to tell us how far you can go to meet interesting people.
		</p>
		<p>
			<strong>Q: How much does this cost?</strong>
			<br />
			A: Setting up your profile and search preferences and scheduling meetings or joining someone else's meeting is totally free. You only pay for your lunch.
		</p>
		<p>
			<strong>Q: What should we talk about during the lunch?</strong>
			<br />
			A: Lunchsparkers are a great community of people just like you. You are matched based on your interests and your reputation level. If you go to the lunch with the attitude of "how can I help you?", you'll have a great time.
		</p>
		<p>
			<strong>Q: How long should the lunch last?</strong>
			<br />
			A: We recommend 45 minutes, so everyone can get back to work. But if you have a great connection with the other person, feel free to hang out later. You are of course welcome to meet people again on your own after that first introduction.
		</p>
		<p>
			<strong>Q: How many lunches can I schedule?</strong>
			<br />
			A: It's really up to you. Active networkers will schedule at least one lunch per week, maybe more. If you are just doing "background networking", you can schedule as little as one lunch per month, that's fine too.
		</p>
		<p>
			<strong>Q: How can I join Lunchsparks?</strong>
			<br />
			A: There are three ways you can join Lunchsparks: if you have an invitation code, if you have a high karma on certain social sites (such as Twitter, Hacker News, Stack Overflow, LinkedIn, etc.), or if an existing user invites you.
		</p>
		<p>
			<strong>Q: How do I cancel a lunch?</strong>
			<br />
			A:We understand that schedules can change, even last minute. But once you have accepted a lunch or someone has accepted a lunch you created, you can only cancel the lunch by directly contacting your lunch partner. Click on your Booked lunch on your calendar dashboard to display that person's contact information. You can cancel an Open lunch by opening that lunch on your calendar and clicking "Cancel".		</p>
		<p>
			<strong>Q: How can I browse all users on Lunchsparks?</strong>
			<br />
			A: We do not allow you to do this. You may only see users who match your search preferences and who have initiated a lunch.
		</p>
		<p>
			<strong>About the VIPs</strong>
		</p>
		<p>
			<strong>Q: How do I meet one of lunchsparks' VIPs?</strong>
			<br />
			A: Each month, we pick the users with the best ratings to meet with our VIPs. In order to be considered, you must have had at least four lunches.
		</p>
		<p>
			<strong>About the Reputation Rating</strong>
		</p>
		<p>
			<strong>Q: Why do you award points and what do I get for accumulating them?</strong>
			<br />
			A: We award points to recognize frequent participation on Lunchsparks and members who receive consistently positive reviews from lunch companions. Consequently, users with a lot of points are likely to be the most useful professional contacts to have and are worth pursuing for lunch meetings. At the moment, the only thing you get for your points are the possibility for more and better lunch matches and, not least, bragging rights.
		</p>
		<p>
			<strong>Q: What does my reputation level mean?</strong>
			<br />
			A: Each user has a level between 1 and 10 (highest). Those levels can change often based on feedback we collect. Initally, your level is based on your social karma, but very quickly feedback from people you met becomes dominant. The higher your reputation level, the more selective the people you meet.
		</p>
		<p>
			<strong>Q: How do I rate someone?</strong>
			<br />
			A: You can only rate someone that you met for lunch through Lunchsparks. After the lunch, you will receive an e-mail asking you for a simple feedback. Just answer truthfully.
		</p>
		<p>
			<strong>Q: Does having more lunches increase my rating?</strong>
			<br />
			A: Not really, quality is more important than quantity. If you have a lot of lunches, your rating will probably reach its true value faster, that's it.
		</p>
